package Dia13.Teoria

/**
 * Esta clase nos sirve como ejemplo para observar el funcionamiento del TestUnit y la documentación.
 *
 * Esta contendrá simplemente funciones para realizar operaciones con enteros.
 */
class Operaciones {

    /**
     * Función que nos permitirá hacer la suma de dos enteros.
     *
     * @param a: valor 1
     * @param b: valor 2
     *
     * @return Int con el resultado de la suma.
     */
    fun sum(a: Int, b: Int) : Int {
        return a + b
    }

    /**
     * Función que nos permitirá hacer la resta de dos enteros.
     *
     * @param a: valor 1
     * @param b: valor 2
     *
     * @return Int con el resultado de la resta.
     */
    fun sub(a: Int, b: Int) : Int {
        return a - b
    }

    /**
     * Función que nos permitirá hacer la multiplicación de dos enteros.
     *
     * @param a: valor 1
     * @param b: valor 2
     *
     * @return Int con el resultado de la multiplicación.
     */
    fun mul(a: Int, b: Int) : Int {
        return a * b
    }

    /**
     * Función que nos permitirá hacer la división de dos enteros.
     *
     * @param a: valor 1
     * @param b: valor 2
     *
     * @return Int con el resultado de la división.
     */
    fun div(a: Int, b: Int) : Int {
        return a/b
    }
}